#!/bin/bash
# =====================================================
# סקריפט הדמיית כניסה למערכת עם הגבלת ניסיונות
# מטרה: תרגול לוגיקה, פונקציות, פקודת loop (while),
# וקודי חזרה (exit codes)
# =====================================================

# ---------- הגדרות משתמש נכונות ----------
USER_OK="roy"
PASS_OK="1234"

# ---------- פונקציית בדיקה ----------
# הפונקציה מקבלת שני ערכים:
# $1 = שם משתמש
# $2 = סיסמה
# תפקידה: לבדוק התאמה ולהחזיר מצב
check_credentials() {
    local input_user="$1"
    local input_pass="$2"

    # אם גם שם המשתמש וגם הסיסמה תואמים
    if [[ "$input_user" == "$USER_OK" && "$input_pass" == "$PASS_OK" ]]; then
        return 0   # הצלחה לוגית
    else
        return 1   # כישלון לוגי
    fi
}

# ---------- משתני שליטה ----------
max_attempts=3        # מספר ניסיונות מקסימלי
attempts=0            # מונה ניסיונות (מתחיל מ-0)

# ---------- פקודת loop (פקודה מחזורית) ----------
# כל עוד מספר הניסיונות קטן מהמקסימום
while [[ $attempts -lt $max_attempts ]]; do

    echo "הכנס שם משתמש:"
    read username

    echo "הכנס סיסמה:"
    read -s password
    echo

    # קריאה לפונקציית הבדיקה
    check_credentials "$username" "$password"

    # בדיקת קוד החזרה של הפונקציה
    if [[ $? -eq 0 ]]; then
        echo "✅ הגישה אושרה"
        exit 0   # יציאה מהסקריפט בהצלחה
    else
        echo "❌ פרטים שגויים"
    fi

    # העלאת מונה הניסיונות
    attempts=$((attempts + 1))

    # ---------- השהייה מדורגת ----------
    # ניסיון 1 → 0 שניות
    # ניסיון 2 → 3 שניות
    # ניסיון 3 → 8 שניות
    # המטרה: האטת ניסיונות אוטומטיים
    sleep $((attempts ** 2 - 1))

done

# ---------- חסימה ----------
# אם הגענו לכאן – כל הניסיונות נוצלו
echo "⛔ הגישה נחסמה – יותר מדי ניסיונות כושלים"
exit 1
